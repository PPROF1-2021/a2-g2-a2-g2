    private void tablaRegistrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaRegistrosMouseClicked
        int fila = tablaRegistros.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Alumno no seleccionado");
        } else {
            id = Integer.parseInt((String) tablaRegistros.getValueAt(fila, 0).toString());
            String nombre = (String) tablaRegistros.getValueAt(fila, 1);
            String apellido = (String) tablaRegistros.getValueAt(fila, 2);
            int dni = Integer.parseInt((String) tablaRegistros.getValueAt(fila, 3).toString());

            txtId.setText("" + id);
            txtNombre.setText(nombre);
            txtApellido.setText(apellido);
            txtDni.setText("" + dni);
    }//GEN-LAST:event_tablaRegistrosMouseClicked
    }

