    void listar() {
        String sql = "select * from alumnos";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] alumnos = new Object[4];
            modelo = (DefaultTableModel) tablaRegistros.getModel();
            while (rs.next()) {
                alumnos[0] = rs.getInt("id_alumno");
                alumnos[1] = rs.getString("nombre");
                alumnos[2] = rs.getString("apellido");
                alumnos[3] = rs.getInt("DNI");
                modelo.addRow(alumnos);
            }
            tablaRegistros.setModel(modelo);
        } catch (Exception e) {
        }
    }

    /*metodo para ingresar nuevo alumno*/
    void Registrar() {
        String dni = txtDni.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        if (dni.equals("") || nombre.equals("") || apellido.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar los Datos Faltantes");
        } else {
            String sql = "insert into alumnos (nombre,apellido,DNI) values('" + nombre + "','" + apellido + "','" + dni + "')";

            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Alumno Registrado con Exito");
                limpiartabla(modelo);

            } catch (Exception e) {
            }
        }

    }
/*metodo para eliminar alumno*/
    void eliminar() {

        int filaselect = tablaRegistros.getSelectedRow();
        if (filaselect == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Registro");
        } else {
            String sql = "delete from alumnos where id_alumno=" + id;
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Registro Eliminado con Exito");
                limpiartabla(modelo);
            } catch (Exception e) {
            }
        }
    }

    /*para borrar datos y listar nuevos*/
    void limpiartabla(DefaultTableModel modelo) {
        for (int i = 0; i <= tablaRegistros.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
